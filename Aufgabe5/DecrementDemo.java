public class DecrementDemo {
    public static void main(String[] args) {
    for(int i = 10; i>-1; i--){
        System.out.println(i);

        }

    System.out.println("---------");

    for(int ii = 0; ii>-6; ii--){
        System.out.println(ii);

        }

    System.out.println("---------");

    int iii = 10;
    while (iii>=0) { 
        System.out.println(iii);
        iii--;
    }

    System.out.println("---------");

    int ip = 0;
    while (ip>=-5) { 
        System.out.println(ip);
        ip--;
    }

    System.out.println("---------");
}
}

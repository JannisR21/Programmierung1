public class IncrementDemo {
    
    public static void main(String[] args) {
    for(int i = 1; i<11; i++){
        System.out.println(i);

        }



    int ii = 0;
    while (ii<10) { 
        System.out.println(ii);
        ii++;
    }

    System.out.println("---------");

    for(int i = -3; i<4; i++){
        System.out.println(i);

        }

    System.out.println("---------");

    int iii = -3;
    while (iii<=3) {  
        System.out.println(iii);
        iii++;
    }
}
}